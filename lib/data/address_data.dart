  /// Get all divisions
  static List<String> getDivisions() {
    return addressData.keys.toList();
  }

  /// Get districts of a specific division
  static List<String> getDistricts(String division) {
    if (addressData.containsKey(division)) {
      return addressData[division]!.keys.toList();
    }
    return [];
  }

  /// Get upazilas of a specific district in a division
  static List<String> getUpazilas(String division, String district) {
    if (addressData.containsKey(division) &&
        addressData[division]!.containsKey(district)) {
      return addressData[division]![district]!.keys.toList();
    }
    return [];
  }

  /// Get unions of a specific upazila in a district of a division
  static List<String> getUnions(
    String division,
    String district,
    String upazila,
  ) {
    if (addressData.containsKey(division) &&
        addressData[division]!.containsKey(district) &&
        addressData[division]![district]!.containsKey(upazila)) {
      return addressData[division]![district]![upazila]!;
    }
    return [];
  }
